CREATE TABLE items (
    id VARCHAR(36) PRIMARY KEY DEFAULT (UUID()),
    name VARCHAR(100) NOT NULL,
    description TEXT,
    weight DECIMAL(10,2) DEFAULT 1.0,
    price DECIMAL(10,2) DEFAULT 0.0,
    item_type ENUM ('WEAPON', 'WEAPON_SLOT', 'ARMOUR', 'CYPHER', 'CYPHER_SLOT', 'ARTIFACT', 'EQUIPMENT', 'ODDITY', 'ODDITY_SLOT', 'AMMUNITION', 'WRIST') NOT NULL,
    equip_location ENUM ('HEAD', 'EYES', 'EARS', 'NECK', 'TORSO', 'LEGS', 'HANDS', 'FOREARM', 'FEET', 'NONE') DEFAULT 'NONE'
)